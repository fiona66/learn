public class Company {
    private String companyName;
    private int companyAge;
    private String companyAddtree;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public int getCompanyAge() {
        return companyAge;
    }

    public void setCompanyAge(int companyAge) {
        this.companyAge = companyAge;
    }

    public String getCompanyAddtree() {
        return companyAddtree;
    }

    public void setCompanyAddtree(String companyAddtree) {
        this.companyAddtree = companyAddtree;
    }
}

